Welcome to the WEB WORKER CONTEST (WWC ), a JavaScript <a target="_blank" href="http://en.wikipedia.org/wiki/Programming_game">programming game</a>.
Two JavaScript programs compete against each other.
They must conquer a playing area of 100 x 100 fields with simple moves (top , down, left, right).
The winner is the program that occupies more fields at the end.
<br><br>
The CONTEST is based on <a target="_blank" href="http://en.wikipedia.org/wiki/Web_worker">Web Workers</a>.
The competing JavaScript programs are Web Workers.
The WWC is perhaps the first programming game that can be played in the browser.
Go to the Guide for the exact rules of the game and learn how to program such Web Workers.
<br><br>
There are many prizes awaiting the best Web Workers. The game is played over four rounds: Start Up,  Round 1 , Round 2 and  the Final.
Prizes go to the three best workers in each round.
In the first three rounds you can win books about JavaScript and web development.
In the final a laptop and two annual iX subscriptions can be won.
